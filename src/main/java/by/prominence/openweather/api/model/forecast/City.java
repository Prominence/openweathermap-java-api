package by.prominence.openweather.api.model.forecast;

import by.prominence.openweather.api.model.Coordinates;
import com.alibaba.fastjson.annotation.JSONField;

import java.util.Objects;

public class City {

    // City ID
    private long id;

    // City name
    private String name;

    @JSONField(name = "coord")
    private Coordinates coordinates;

    // Country code (GB, JP etc.)
    private String country;

    public long getId() {
        return id;
    }

    public void setId(long id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public Coordinates getCoordinates() {
        return coordinates;
    }

    public void setCoordinates(Coordinates coordinates) {
        this.coordinates = coordinates;
    }

    public String getCountry() {
        return country;
    }

    public void setCountry(String country) {
        this.country = country;
    }

    @Override
    public String toString() {
        return "City{" +
                "id=" + id +
                ", name='" + name + '\'' +
                ", coordinates=" + coordinates +
                ", country='" + country + '\'' +
                '}';
    }

    @Override
    public boolean equals(Object o) {
        if (this == o) return true;
        if (o == null || getClass() != o.getClass()) return false;
        City city = (City) o;
        return id == city.id &&
                Objects.equals(name, city.name) &&
                Objects.equals(coordinates, city.coordinates) &&
                Objects.equals(country, city.country);
    }

    @Override
    public int hashCode() {

        return Objects.hash(id, name, coordinates, country);
    }
}
